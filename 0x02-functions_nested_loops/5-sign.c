#include "main.h"

/**
 * print_sign - finds the value of character
 * @n: argument passed
 * Return: a sign as per set conditions
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
